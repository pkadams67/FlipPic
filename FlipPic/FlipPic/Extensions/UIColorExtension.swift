import UIKit

extension UIColor {
    
    static func flipPicBlue() -> UIColor {
        UIColor(red: 0.1294, green: 0.5922, blue: 0.9529, alpha: 1.0) // Hex #2196F3
    }
    
    static func flipPicGray() -> UIColor {
        UIColor(red: 0.490, green: 0.490, blue: 0.490, alpha: 1.0) // Hex #7D7D7D
    }
    
    static func flipPicGreen() -> UIColor {
        UIColor(red: 0.545, green: 0.765, blue: 0.290, alpha: 1.0) // Hex #8BC34A
    }
}
