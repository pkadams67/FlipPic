import UIKit

extension UIColor {
	static func flipPicGreen() -> UIColor {
		UIColor(red: 147 / 255, green: 206 / 255, blue: 59 / 255, alpha: 1) // Hex #93ce3b
	}

	static func flipPicBlue() -> UIColor {
		UIColor(red: 38 / 255, green: 99 / 255, blue: 181 / 255, alpha: 1) // Hex #2663b5
	}

	static func flipPicGray() -> UIColor {
		UIColor(red: 60 / 255, green: 63 / 255, blue: 66 / 255, alpha: 1) // Hex #3c3f42
	}
}
